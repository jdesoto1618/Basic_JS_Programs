//This program is very limited in function, due to having only four cases.

function helloWorld (code) {
  switch(code.toLowerCase()) {
    case 'es':
    alert("Hola, Mundo!");
    break;

    case 'de':
    alert("Hallo, Welt");
    break;

    case 'en':
    alert("Hello, World");
    break;

    default:
    alert("Hello, World");
  }
}
